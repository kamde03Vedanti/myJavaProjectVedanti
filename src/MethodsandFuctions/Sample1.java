package MethodsandFuctions;

public class Sample1 {

	    
	public static void x(int a, int b, int c) // static method with parameter
	{   
		

		int sum=a+b+c;
		System.out.println(" Sum of static method x " +sum);
		System.out.println(" ");
		
	}
	public void y()  //Non Static Method without Parameter
	
	{
		int m;
		int n;
		int o;
		int sub;
		m=40;
		n=60;
		o=30;
		sub=m-n-o;
		
		System.out.println("Substraction of Non Static Method y " +sub);
		System.out.println(" ");
		System.out.println("**************************************");
	}
	
	
	 
	
}
