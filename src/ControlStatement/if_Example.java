package ControlStatement;

public class if_Example {

	public static void main(String[] args) 
	{
		//if num is even or odd
		int num=3;
		if(num % 2==0)
		{
			System.out.println("The number is even " +num);
		}
		else
		{
			System.out.println("The number is odd " +num);
		}
	}

}
