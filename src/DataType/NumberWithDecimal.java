package DataType;

public class NumberWithDecimal {

	public static void main(String[] args) {
		
		float height;
		height=5.2f;     							// f is for float 
		System.out.println("My height is" +height);
		
		
		double distance;
		distance=25.98765432100d;                  // d is for double
		System.out.println("Distance is " +distance);
		
	}
}
 
