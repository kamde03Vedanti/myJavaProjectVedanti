package LoopStatement;

public class whileUse {

	public static void main(String[] args)
	{
		
		//initialization 
		 int m=5;
		 
	    while(m<=50) // condition
	    	
	    {
	    	//statement
	    	System.out.println(m);
	    	
	    	// updation
	    	m=m+5;
	    }
	    System.out.println("*************");
	    int n=50;
	    while(n>=5)
	    {
	    	System.out.println(n);
	    	n=n-5;
	    }
	    
	   	}

	
	
}
