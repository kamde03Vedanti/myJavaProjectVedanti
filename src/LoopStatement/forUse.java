package LoopStatement;

public class forUse {

	public static void main(String[] args) 
	{
		//Initialization  condition  updation 
	    //  i=15            <=150       i=i+15
		
		for(int i=15;i<=150;i=i+15)
		{
			System.out.println(i);
			System.out.println("=================");
		}
		
		
	}
	
	
}